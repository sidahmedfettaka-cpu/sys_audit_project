# =============================================================================
# FUNCTION: build_json_report
# PURPOSE : Write the report in JSON format for machine readability
# ARGS    : $1 = output file path, $2 = report type
# =============================================================================
build_json_report() {
    local outfile="$1"
    local report_type="$2"
    local timestamp
    timestamp=$(date '+%Y-%m-%dT%H:%M:%S')

    # We use printf with escaped values to build valid JSON
    # Note: this is a simplified JSON builder - for production you'd use jq
    {
        printf '{\n'
        printf '  "report_metadata": {\n'
        printf '    "type": "%s",\n' "$report_type"
        printf '    "hostname": "%s",\n' "$HOSTNAME_VAL"
        printf '    "timestamp": "%s",\n' "$timestamp"
        printf '    "generator": "NSCS Audit System"\n'
        printf '  },\n'
        printf '  "hardware": {\n'
        printf '    "cpu_model": "%s",\n' "$(echo "$HW_CPU" | sed 's/"/\\"/g')"
        printf '    "cpu_cores": "%s",\n' "$HW_CPU_CORES"
        printf '    "cpu_arch": "%s",\n' "$HW_CPU_ARCH"
        printf '    "gpu": "%s",\n' "$(echo "$HW_GPU" | head -1 | sed 's/"/\\"/g')"
        printf '    "ram_total_mb": "%s",\n' "$HW_RAM_TOTAL"
        printf '    "ram_used_mb": "%s",\n' "$HW_RAM_USED"
        printf '    "ram_free_mb": "%s",\n' "$HW_RAM_FREE"
        printf '    "motherboard": "%s"\n' "$(echo "$HW_MOTHERBOARD" | sed 's/"/\\"/g')"
        printf '  },\n'
        printf '  "software": {\n'
        printf '    "os_name": "%s",\n' "$SW_OS_NAME"
        printf '    "os_version": "%s",\n' "$SW_OS_VERSION"
        printf '    "kernel": "%s",\n' "$SW_KERNEL"
        printf '    "arch": "%s",\n' "$SW_ARCH"
        printf '    "uptime": "%s",\n' "$(echo "$SW_UPTIME" | sed 's/"/\\"/g')"
        printf '    "shell": "%s",\n' "$SW_SHELL"
        printf '    "package_count": "%s"\n' "$SW_PACKAGE_COUNT"
        printf '  }\n'
        printf '}\n'
    } > "$outfile"
}

# =============================================================================
# FUNCTION: generate_report
# PURPOSE : Orchestrate report generation in all formats (txt, html, json)
# ARGS    : $1 = report type ("short" or "full")
# =============================================================================
generate_report() {
    local report_type="${1:-short}"
    local timestamp
    timestamp=$(get_timestamp)

    # Make sure the report directory exists
    mkdir -p "$REPORT_DIR"

    # Build file names using hostname + timestamp + type
    local base_name="${HOSTNAME_VAL}_${report_type}_${timestamp}"
    local txt_file="$REPORT_DIR/${base_name}.txt"
    local html_file="$REPORT_DIR/${base_name}.html"
    local json_file="$REPORT_DIR/${base_name}.json"

    section_header "Generating ${report_type^^} Report"

    # --- Generate TXT ---
    echo -e "${YELLOW}Creating TXT report...${NC}"
    build_txt_report "$txt_file" "$report_type"
    if [ -f "$txt_file" ]; then
        echo -e "${GREEN}  ✓ TXT  → $txt_file${NC}"
        LAST_REPORT_TXT="$txt_file"
        # Create a SHA256 hash for integrity checking
        verify_log_integrity "$txt_file" "create"
    fi

    # --- Generate HTML ---
    echo -e "${YELLOW}Creating HTML report...${NC}"
    build_html_report "$html_file" "$report_type"
    if [ -f "$html_file" ]; then
        echo -e "${GREEN}  ✓ HTML → $html_file${NC}"
        LAST_REPORT_HTML="$html_file"
    fi

    # --- Generate JSON ---
    echo -e "${YELLOW}Creating JSON report...${NC}"
    build_json_report "$json_file" "$report_type"
    if [ -f "$json_file" ]; then
        echo -e "${GREEN}  ✓ JSON → $json_file${NC}"
        LAST_REPORT_JSON="$json_file"
    fi

    echo ""
    echo -e "${GREEN}All reports saved to: $REPORT_DIR${NC}"
    log_message "INFO" "Reports generated: $base_name (.txt, .html, .json)"

    # Rotate old reports: keep only the last 20
    rotate_logs "$REPORT_DIR" 20
}
