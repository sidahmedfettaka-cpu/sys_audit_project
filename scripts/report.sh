# Will hold the path to the last generated report (used by email module)
LAST_REPORT_TXT=""
LAST_REPORT_HTML=""
LAST_REPORT_JSON=""

# =============================================================================
# FUNCTION: build_txt_report
# PURPOSE : Write the report content as plain text to a file
# ARGS    : $1 = output file path, $2 = report type ("short" or "full")
# =============================================================================
build_txt_report() {
    local outfile="$1"
    local report_type="$2"
    local date_str
    date_str=$(get_date_readable)
    local sep="============================================================"
    local thin_sep="------------------------------------------------------------"

    {
        # ---- Report Header ----
        echo "$sep"
        echo "  LINUX SYSTEM AUDIT REPORT"
        echo "  Type     : $(echo "$report_type" | tr '[:lower:]' '[:upper:]') REPORT"
        echo "  Hostname : $HOSTNAME_VAL"
        echo "  Date     : $date_str"
        echo "  Generated by: NSCS Audit System"
        echo "$sep"
        echo ""

        # ========== HARDWARE SECTION ==========
        echo "$sep"
        echo "  SECTION 1 : HARDWARE INFORMATION"
        echo "$sep"
        echo ""

        echo "[ CPU ]"
        echo "$thin_sep"
        echo "  Model        : ${HW_CPU:-N/A}"
        echo "  Cores        : ${HW_CPU_CORES:-N/A}"
        echo "  Architecture : ${HW_CPU_ARCH:-N/A}"
        echo ""

        echo "[ GPU ]"
        echo "$thin_sep"
        echo "  ${HW_GPU:-N/A}"
        echo ""

        echo "[ MEMORY (RAM) ]"
        echo "$thin_sep"
        echo "  Total     : ${HW_RAM_TOTAL:-N/A} MB"
        echo "  Used      : ${HW_RAM_USED:-N/A} MB"
        echo "  Available : ${HW_RAM_FREE:-N/A} MB"
        echo ""

        echo "[ DISK / STORAGE ]"
        echo "$thin_sep"
        if [ "$report_type" = "full" ]; then
            echo "  Block Devices:"
            echo "${HW_PARTITIONS:-N/A}" | sed 's/^/    /'
            echo ""
            echo "  Disk Usage:"
            echo "${HW_DISK:-N/A}" | sed 's/^/    /'
        else
            # Short report: just show disk usage summary
            echo "${HW_DISK:-N/A}" | sed 's/^/  /'
        fi
        echo ""

        echo "[ NETWORK INTERFACES ]"
        echo "$thin_sep"
        echo "${HW_NETWORK:-N/A}" | sed 's/^/  /'
        echo ""

        if [ "$report_type" = "full" ]; then
            echo "[ MAC & IP ADDRESSES ]"
            echo "$thin_sep"
            echo "${HW_MAC_IP:-N/A}" | sed 's/^/  /'
            echo ""
        fi

        echo "[ MOTHERBOARD ]"
        echo "$thin_sep"
        echo "  ${HW_MOTHERBOARD:-N/A}"
        echo ""

        if [ "$report_type" = "full" ]; then
            echo "[ USB DEVICES ]"
            echo "$thin_sep"
            echo "${HW_USB:-N/A}" | sed 's/^/  /'
            echo ""
        fi

        # ========== SOFTWARE SECTION ==========
        echo "$sep"
        echo "  SECTION 2 : OPERATING SYSTEM & SOFTWARE INFORMATION"
        echo "$sep"
        echo ""

        echo "[ OPERATING SYSTEM ]"
        echo "$thin_sep"
        echo "  OS Name      : ${SW_OS_NAME:-N/A}"
        echo "  OS Version   : ${SW_OS_VERSION:-N/A}"
        echo "  Kernel       : ${SW_KERNEL:-N/A}"
        echo "  Architecture : ${SW_ARCH:-N/A}"
        echo "  Uptime       : ${SW_UPTIME:-N/A}"
        echo "  Shell        : ${SW_SHELL:-N/A}"
        echo ""

        echo "[ INSTALLED PACKAGES ]"
        echo "$thin_sep"
        echo "  Total installed: ${SW_PACKAGE_COUNT:-N/A}"
        if [ "$report_type" = "full" ]; then
            echo ""
            echo "  Package list:"
            # Show all packages (can be long in full report)
            echo "${SW_PACKAGES:-N/A}" | sed 's/^/    /'
        else
            # Short report: show only first 20 packages
            echo ""
            echo "  First 20 packages:"
            echo "${SW_PACKAGES:-N/A}" | head -20 | sed 's/^/    /'
            echo "    ... (use full report to see all)"
        fi
        echo ""

        echo "[ LOGGED-IN USERS ]"
        echo "$thin_sep"
        echo "${SW_LOGGED_USERS:-N/A}" | sed 's/^/  /'
        echo ""

        echo "[ OPEN PORTS ]"
        echo "$thin_sep"
        echo "${SW_OPEN_PORTS:-N/A}" | sed 's/^/  /'
        echo ""

        if [ "$report_type" = "full" ]; then
            echo "[ RUNNING SERVICES ]"
            echo "$thin_sep"
            echo "${SW_SERVICES:-N/A}" | sed 's/^/  /'
            echo ""

            echo "[ TOP ACTIVE PROCESSES (by CPU) ]"
            echo "$thin_sep"
            echo "${SW_PROCESSES:-N/A}" | sed 's/^/  /'
            echo ""
        fi

        # ---- Footer ----
        echo "$sep"
        echo "  END OF REPORT"
        echo "$sep"
        echo ""

    } > "$outfile"
}

